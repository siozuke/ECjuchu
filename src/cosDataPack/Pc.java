	/** ドライブ */
	private String drive;
	/** ネットワーク */
	private String network;
	/** OS */
	private String os;
	/** 統合ソフト */
	private String soft;
	/** バッテリ */
	private String battery;
	/** 付属品 */
	private String accessory;
	/** 保証期間 */
